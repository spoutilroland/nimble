const SOCIAL_LABELS: Record<string, string> = {
  linkedin: 'LinkedIn', facebook: 'Facebook', instagram: 'Instagram',
  x: 'X', tiktok: 'TikTok', youtube: 'YouTube',
  pinterest: 'Pinterest', github: 'GitHub',
};

interface Props {
  direction?: string;
  shape?: string;
  size?: string;
}

export function FooterSocial({ direction = 'horizontal', shape = 'round', size = 'md' }: Props) {
  const ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    fetch('/api/site')
      .then((r) => r.json())
      .then((site) => {
        if (!site.social || !ref.current) return;
        Object.entries(site.social).forEach(([net, url]) => {
          if (!url) return;
          const a = document.createElement('a');
          a.href = url as string;
          a.target = '_blank';
          a.rel = 'noopener noreferrer';
          a.className = `social-btn ${shape} size-${size}`;
          a.title = SOCIAL_LABELS[net] || net;
          a.innerHTML = SOCIAL_ICONS[net] || net.charAt(0).toUpperCase();
          ref.current!.appendChild(a);
        });
      })
      .catch(() => {});
  }, [shape, size]);

  return (
    <div
      ref={ref}
      className={`layout-social layout-social-${direction}`}
      data-social-shape={shape}
      data-social-size={size}
    />
  );
}
